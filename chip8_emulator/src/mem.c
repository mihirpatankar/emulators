/*==============================================================================
 Include Files
 =============================================================================*/

#include "mem.h"

/*==============================================================================
 Macros
 =============================================================================*/

#define FONTSET_SIZE 80
#define LOAD_FONTSET_OFFSET 80

/*==============================================================================
 Static data
 =============================================================================*/

static uint8_t fontset[FONTSET_SIZE] =
{
  0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
  0x20, 0x60, 0x20, 0x20, 0x70, // 1
  0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
  0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
  0x90, 0x90, 0xF0, 0x10, 0x10, // 4
  0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
  0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
  0xF0, 0x10, 0x20, 0x40, 0x40, // 7
  0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
  0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
  0xF0, 0x90, 0xF0, 0x90, 0x90, // A
  0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
  0xF0, 0x80, 0x80, 0x80, 0xF0, // C
  0xE0, 0x90, 0x90, 0x90, 0xE0, // D
  0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
  0xF0, 0x80, 0xF0, 0x80, 0x80  // F
};

/*==============================================================================
 Macros
 =============================================================================*/

#define MEMORY_INIT 0

/*==============================================================================
 Static functions
 =============================================================================*/

static void load_fontset(memory* memory)
{
  for(int i = 0; i < FONTSET_SIZE; i++)
  {
    memory->RAM[i+LOAD_FONTSET_OFFSET] = fontset[i];
  }
}

/*==============================================================================
 Function definitions
 =============================================================================*/

void initialize_memory(memory* memory)
{
  // Initialize RAM
  for(int i = 0; i < RAM_SIZE; i++)
  {
    memory->RAM[i] = MEMORY_INIT;
  }

  // Initialize stack
  for(int i = 0; i < STACK_SIZE; i++)
  {
    memory->stack[i] = MEMORY_INIT;
  }

  load_fontset(memory);
}
